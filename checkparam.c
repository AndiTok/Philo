#include "philo.h"

int	checkparam(char **av)
{
	int	i;
	int	j;

	i = 1;
	while (av[i] != 0x00)
	{
		j = 0;
		while (av[i][j] != 0x00)
		{
			if (!(av[i][j] >= '0' && av[i][j] <= '9') || av[i][j] == '-')
			{
				printf("check param\n");
				return (0);
			}
			j++;
		}
		i++;
	}
	return (1);
}
